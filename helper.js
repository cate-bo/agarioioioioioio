class circle
{
    x;
    y;
    size;

    constructor(x, y, size)
    {
        this.x = x;
        this.y = y;
        this.size = size;
    }
}

class point
{
    x;
    y;

    constructor(x, y)
    {
        this.x = x;
        this.y = y;
    }
}

class direction
{
    lengthx;
    lengthy;

    constructor(lengthx, lengthy)
    {
        this.lengthx = lengthx;
        this.lengthy = lengthy;
    }
}